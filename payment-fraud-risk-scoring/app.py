class ScoreResponse(BaseModel):
    fraud_probability: float
    risk_score: int
    risk_band: str
    is_flagged: int
    threshold: float


class HealthResponse(BaseModel):
    status: Literal["ok", "degraded"]
    model_loaded: bool
    model_path: str
    rate_limit_per_min: int


def _get_scorer(request: Request) -> FraudScorer:
    scorer = request.app.state.scorer
    if scorer is None:
        raise HTTPException(
            status_code=503,
            detail=(
                "Fraud scorer model is not loaded. "
                "Train and save models/fraud_scorer.joblib, then restart or reload."
            ),
        )
    return scorer


@app.get("/health", response_model=HealthResponse)
def health(request: Request) -> HealthResponse:
    """Liveness and model-readiness check."""
    loaded = request.app.state.scorer is not None
    return HealthResponse(
        status="ok" if loaded else "degraded",
        model_loaded=loaded,
        model_path=str(request.app.state.model_path),
        rate_limit_per_min=int(request.app.state.rate_limiter.limit_per_window),
    )


@app.post(
    "/admin/reload-model",
    response_model=HealthResponse,
    dependencies=[Depends(_auth_guard)],
)
def reload_model(request: Request) -> HealthResponse:
    """Reload scorer artifact from disk without restarting the process."""
    request.app.state.scorer = _load_scorer(request.app.state.model_path)
    return health(request)


@app.post(
    "/score",
    response_model=ScoreResponse,
    dependencies=[Depends(_auth_guard), Depends(_rate_limit_guard)],
)
def score(txn: Transaction, request: Request) -> ScoreResponse:
    """Score a single transaction."""
    scorer = _get_scorer(request)
    row = pd.DataFrame([txn.model_dump(mode="python")])

    try:
        scored = scorer.score_frame(row, copy_input=False).iloc[0]
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc
    except HTTPException:
        raise
    except Exception as exc:  # pragma: no cover - defensive wrapper
        LOGGER.exception("Unexpected scoring failure.")
        raise HTTPException(status_code=500, detail="Internal scoring error.") from exc

    return ScoreResponse(
        fraud_probability=float(scored["fraud_probability"]),
        risk_score=int(scored["risk_score"]),
        risk_band=str(scored["risk_band"]),
        is_flagged=int(scored["is_flagged"]),
        threshold=float(scorer.threshold),
    )
